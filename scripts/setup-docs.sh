RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Logging functions
log_info() {
    echo -e "${BLUE}ℹ️  $1${NC}"
}

log_success() {
    echo -e "${GREEN}✅ $1${NC}"
}

log_warning() {
    echo -e "${YELLOW}⚠️  $1${NC}"
}

log_error() {
    echo -e "${RED}❌ $1${NC}"
}

# Function to create directory if it doesn't exist
create_directory() {
    local dir_path="$1"
    local description="$2"
    
    if [ ! -d "$dir_path" ]; then
        log_info "Creating directory: $dir_path ($description)"
        mkdir -p "$dir_path"
        log_success "Created: $dir_path"
    else
        log_success "Directory exists: $dir_path"
    fi
}

# Function to create file if it doesn't exist
create_file() {
    local file_path="$1"
    local content="$2"
    local description="$3"
    
    if [ ! -f "$file_path" ]; then
        log_info "Creating file: $file_path ($description)"
        echo -e "$content" > "$file_path"
        log_success "Created: $file_path"
    else
        log_success "File exists: $file_path"
    fi
}

# Function to create directory with README
create_directory_with_readme() {
    local dir_path="$1"
    local description="$2"
    local readme_content="$3"
    
    create_directory "$dir_path" "$description"
    create_file "$dir_path/README.md" "$readme_content" "Directory README"
}

