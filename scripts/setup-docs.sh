## 🔤 Algorithm Terms

### A
**Algorithm**: A step-by-step procedure for solving a problem or accomplishing a task.

**Array**: A fixed-size collection of elements of the same type stored in contiguous memory locations.

**Asymptotic Analysis**: Method of describing the limiting behavior of an algorithm when the input size tends towards infinity.

### B
**Big-O Notation**: Mathematical notation describing the limiting behavior of a function when the argument tends towards a particular value or infinity.

**Binary Search**: Search algorithm that finds the position of a target value within a sorted array.

**Bubble Sort**: Simple sorting algorithm that repeatedly steps through the list, compares adjacent elements and swaps them if they are in the wrong order.

### C
**Complexity**: Measure of the resources required by an algorithm, typically time and space.

**Constant Time (O(1))**: Algorithm execution time that does not depend on input size.

### D
**Data Structure**: Particular way of organizing and storing data in a computer so that it can be accessed and modified efficiently.

**Divide and Conquer**: Algorithm design paradigm that recursively breaks down a problem into two or more sub-problems.

### E
**Edge Case**: Test case at the extreme operating conditions of an algorithm.

### F
**First-In-First-Out (FIFO)**: Data ordering principle where the first element added to the collection will be the first one to be removed.

### G
**Graph**: Abstract data type consisting of a set of vertices and a set of edges connecting pairs of vertices.

### H
**Heap**: Specialized tree-based data structure that satisfies the heap property.

### I
**In-Place Algorithm**: Algorithm that transforms input using no auxiliary data structure.

**Insertion Sort**: Simple sorting algorithm that builds the final sorted array one item at a time.

### L
**Last-In-First-Out (LIFO)**: Data ordering principle where the last element added to the collection will be the first one to be removed.

**Linear Search**: Search algorithm that sequentially checks each element in a list until a match is found.

**Linked List**: Data structure consisting of nodes where each node contains data and a pointer to the next node.

### M
**Merge Sort**: Divide-and-conquer algorithm that divides the array into two halves, recursively sorts them, and then merges them.

**Memory Complexity**: Amount of memory an algorithm needs to run to completion.

### O
**Optimization**: Process of modifying a system to make some features of it work more efficiently or use fewer resources.

### P
**Performance**: Measure of how efficiently a computer system uses available resources.

**Priority Queue**: Abstract data type similar to a regular queue but where each element has a priority associated with it.

### Q
**Queue**: Collection of entities that are maintained in a sequence and can be modified by the addition of entities at one end and removal from the other.

**Quick Sort**: Divide-and-conquer sorting algorithm that picks an element as a pivot and partitions the array around it.

### R
**Recursion**: Method where the solution to a problem depends on solutions to smaller instances of the same problem.

### S
**Search Algorithm**: Algorithm for retrieving information stored within some data structure, or calculated in the search space of a problem domain.

**Selection Sort**: In-place comparison sorting algorithm with O(n²) time complexity.

**Space Complexity**: Amount of memory space required by an algorithm to run to completion.

**Stack**: Abstract data type that serves as a collection of elements, with two main principal operations: push and pop.

**Stable Sort**: Sorting algorithm that maintains the relative order of equal elements.

### T
**Time Complexity**: Computational complexity that describes the amount of time it takes to run an algorithm.

**Tree**: Abstract data type that simulates a hierarchical tree structure, with a root value and subtrees of children with a parent-child relationship.

