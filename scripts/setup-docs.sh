#### Best Practices
- **Null Checks**: Always check for null inputs
- **Boundary Validation**: Validate array sizes, ranges
- **Type Checking**: Ensure correct data types
- **Value Validation**: Check for valid values

### 2. Resource Management

#### Pattern
```java
public void processFile(String filename) {
    FileInputStream fis = null;
    try {
        fis = new FileInputStream(filename);
        // Process file
        // ...
    } catch (FileNotFoundException e) {
        throw new RuntimeException("File not found: " + filename, e);
    } catch (IOException e) {
        throw new RuntimeException("Error reading file: " + filename, e);
    } finally {
        if (fis != null) {
            try {
                fis.close();
            } catch (IOException e) {
                // Log but don't throw
                System.err.println("Warning: Failed to close file: " + e.getMessage());
            }
        }
    }
}
```

#### Best Practices
- **Try-Catch-Finally**: Ensure resource cleanup
- **Specific Exceptions**: Catch specific exception types
- **Resource Cleanup**: Always close resources
- **Nested Exceptions**: Preserve original exception

### 3. Algorithm-Specific Errors

#### Pattern
```java
public int binarySearch(int[] arr, int target) {
    // Validate input
    if (arr == null) {
        throw new IllegalArgumentException("Array cannot be null");
    }
    
    if (!isSorted(arr)) {
        throw new IllegalArgumentException("Array must be sorted for binary search");
    }
    
    // Algorithm implementation
    int left = 0, right = arr.length - 1;
    
    while (left <= right) {
        int mid = left + (right - left) / 2;
        
        if (arr[mid] == target) {
            return mid;
        } else if (arr[mid] < target) {
            left = mid + 1;
        } else {
            right = mid - 1;
        }
    }
    
    // Return sentinel value for not found
    return -1;
}

private boolean isSorted(int[] arr) {
    for (int i = 1; i < arr.length; i++) {
        if (arr[i] < arr[i - 1]) {
            return false;
        }
    }
    return true;
}
```

#### Best Practices
- **Precondition Checks**: Validate algorithm requirements
- **Postcondition Validation**: Verify results
- **Sentinel Values**: Use clear not-found indicators
- **Helper Methods**: Extract validation logic

