## 📚 Algorithm Questions

### Q: Which sorting algorithm is fastest?
**A**: It depends on the use case:
- **Quick Sort**: Fastest average case, O(n log n)
- **Merge Sort**: Guaranteed O(n log n), stable
- **Insertion Sort**: Best for nearly sorted data, O(n) best case

### Q: When should I use binary search?
**A**: Use binary search when:
- Data is sorted
- Multiple searches will be performed
- Large dataset (> 100 elements)
- Search time is critical

### Q: What's the difference between array and linked list?
**A**: 
| Feature | Array | Linked List |
|---------|-------|-------------|
| **Access** | O(1) random | O(n) sequential |
| **Insert/Delete** | O(n) | O(1) at beginning |
| **Memory** | Contiguous | Distributed |
| **Size** | Fixed | Dynamic |

